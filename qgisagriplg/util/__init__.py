def defineVar(v):
    """Ensure variable is defined"""
    try:
        v
    except NameError:
        v = None

# --------------------------------------
# 
# -------------------------------------- 
def isVarDefined(v):
    """Check if variable is defined"""
    try:
        v
    except NameError:
        return False
    return True
